package day10_loops_review;

public class Task1 {

	public static void main(String[] args) {
		// Write a program to print number Between 20 to 30. 

		
		
		
		int count = 20;
		
		while (count <= 30) {
			System.out.println(count);
			count++;
		}
		
		count = 30;
		do {
			System.out.println(count);
			count--;
		}while (count >= 20);
		
		
		 for (int i = 20; i < 31; i++) {
	            System.out.println(i);
	        }
		
		
		
			
	}

}
